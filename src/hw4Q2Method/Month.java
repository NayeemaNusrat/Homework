public class Month {

	public static void main(String[] args) {
		String month = "November";

		switch (month) {

		case "January":
			System.out.println("January is my birth month");
			break;
		case "February":
			System.out.println("February is my birth month");
			break;
		case "March":
			System.out.println("March is my birth month");
			break;
		case "April":
			System.out.println("April is my birth month");
			break;
		case "May":
			System.out.println("May is my birth month");
			break;
		case "June":
			System.out.println("June is my birth month");
			break;
		case "July":
			System.out.println("July is my birth month");
			break;
		case "August":
			System.out.println("August is my birth month");
			break;
		case "September":
			System.out.println("September is my birth month");
			break;
		case "October":
			System.out.println("October is my birth month");
			break;
		case "November":
			System.out.println("November is my birth month");
			break;
		case "December":
			System.out.println("December is my birth month");
			break;
		default:
			System.out.println("Please put a valid month");
			break;

		}
	}
}